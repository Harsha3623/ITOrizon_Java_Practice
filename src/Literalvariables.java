public class Literalvariables {
    public static void main(String[] args) {
        System.out.println(15);//decimal
        System.out.println(017);//octal
        System.out.println(0xF);//hexadecimal
        System.out.println(0B1111);//binary

        /**
         * floating point literal
         */

        System.out.println(74589787.12);//Standard
        System.out.println(7.458978712E7);//Scientific

        /**
         * Boolean literals
         */
        System.out.println(true);
        System.out.println(false);

        /**
         * Charecter literal
         *
         */
        System.out.println("//\n");
        System.out.println("//\\n//");
        System.out.println("//\t//");
        System.out.println("//\\t//");

        /**
         * String literal
         * sequence of charecter is known as string
         * string literal value is placed inside the double quotes mark
         */
        System.out.println("");//emptystring
        System.out.println(" ");//Single space string
        System.out.println("A");//String Containing a Single character
        System.out.println("Java");//String

    }
}
